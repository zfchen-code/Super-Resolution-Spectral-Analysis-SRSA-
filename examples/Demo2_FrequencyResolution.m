% *[Overview]
%  This script illustrates the high frequency-resolution capability of SRSA using two real-valued
%  multitone signals composed of five and nine closely spaced sinusoidal components, respectively.
%  Because each real-valued sinusoid corresponds to a conjugate pair of complex exponentials, the
%  ground-truth model orders are K1_true = 10 for y1 and K2_true = 18 for y2.
%
%  In practical applications, however, the true model order is generally unknown. The present
%  experiment therefore uses the rule-based estimate K1_input = K2_input = 180, identified from
%  the elbow region of the corresponding singular-value spectra. The comparison between the known
%  model orders and the adopted rule-based values also illustrates the ability of SRSA to retain
%  closely spaced spectral components when K_input is conservatively overestimated.
%
% *[Caption for Figure (1)]
%      Panel (a): Simulated multitone signal y1.
%      Panel (b): Singular-value spectrum of the Hankel matrix constructed from y1, showing the
%                 ground-truth model order K1_true = 10 and the rule-based value K1_input = 180.
%      Panel (c): Full 2-D SRSA coherence response of y1 computed with K1_input = 180.
%      Panel (d): Bilateral Fourier amplitude spectrum of y1.
%      Panel (e): Full 2-D SRSA coherence response of y2 computed with K2_input = 180.
%      Panel (f): Bilateral Fourier amplitude spectrum of y2.
%      Panel (g): Simulated multitone signal y2.
%      Panel (h): Singular-value spectrum of the Hankel matrix constructed from y2, showing the
%                 ground-truth model order K2_true = 18 and the rule-based value K2_input = 180.
%      Panel (i): Selected Q_L range of the 2-D SRSA coherence response shown in panel (c).
%      Panel (j): Logarithmic stacked 1-D SRSA spectrum of y1.
%      Panel (k): Selected Q_L range of the 2-D SRSA coherence response shown in panel (e).
%      Panel (l): Logarithmic stacked 1-D SRSA spectrum of y2.
%
% *[Computational Environment]
%  The calculations were performed using MATLAB R2024b Update 3 on a workstation equipped with an
%  Intel Core i9-14900K processor (24 cores and 32 threads), 128 GB of RAM, and an NVIDIA GeForce
%  RTX 4090 D GPU with 24 GB of memory. The process-based parallel pool used eight workers. The
%  present implementation does not invoke GPU computing; therefore, the GPU did not contribute to
%  the reported runtimes.
%
% *[Representative Runtime]
%  The expected wall-clock time for this full-SVD demo is approximately 25 min on the stated
%  workstation. The runtime is dominated by the repeated complete SVDs of the Hankel matrices and
%  may vary with the parallel configuration and available memory.
